// For loops
for (let i = 1; i <= 5; i++) {
    console.log('For: Hello World!')
}


// While Loops
let i = 0;
while (i < 5) {
    console.log('While: Hello World');
    i++;
}



// do-while loops
let I = 0;          // capital I just because i is taken
do {
    console.log('Do: Hello World!')
    I++
} while (I < 5);